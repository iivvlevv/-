def count_unique_chars(s):
    """
    Возвращает количество уникальных символов в строке.
    
    :param s: Входная строка.
    :return: Количество уникальных символов.
    """
    # Используем множество для хранения уникальных символов
    unique_chars = set()
    
    # Переберите символы строки и добавьте их в множество
    for char in s:
        unique_chars.add(char)
    
    # Возвращаем размер множества, который представляет количество уникальных символов
    return len(unique_chars)

# Пример использования
print(count_unique_chars("hello"))  # Выводит: 4

# Альтернативная реализация в одну строку
def count_unique_chars_alt(s):
    return len(set(s))

# Пример использования альтернативной реализации
print(count_unique_chars_alt("hello"))  # Выводит: 4
